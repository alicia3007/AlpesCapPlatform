CREATE TABLE Revision (
    idRevision NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY
               CONSTRAINT PK_Revision PRIMARY KEY,
    idServicio NUMBER NOT NULL,
    idRevisorUsuario NUMBER NOT NULL,
    idReceptorUsuario NUMBER NOT NULL,
