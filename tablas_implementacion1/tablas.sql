-- Tabla de Disponibilidad (requerida por RF5)
-- Nota: Se modela con campos de fecha/hora como texto (YYYY-MM-DD y HH:MM) para
-- que las comparaciones utilizadas por la aplicación funcionen de forma determinística.
CREATE TABLE Disponibilidad (
    id           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY
                 CONSTRAINT PK_Disponibilidad PRIMARY KEY,
    idConductor  NUMBER NOT NULL,
    idVehiculo   NUMBER NOT NULL,
    tipoServicio VARCHAR2(20) NOT NULL
                 CONSTRAINT CK_Disponibilidad_TipoServicio
                 CHECK (tipoServicio IN ('PASAJEROS','DOMICILIO','MERCANCIAS')),
    diaInicio    VARCHAR2(10) NOT NULL, -- formato esperado: YYYY-MM-DD
    diaFin      VARCHAR2(10) NOT NULL,  -- formato esperado: YYYY-MM-DD
    horaInicio  VARCHAR2(5)  NOT NULL,  -- formato esperado: HH:MM (con cero inicial)
    horaFin     VARCHAR2(5)  NOT NULL,  -- formato esperado: HH:MM (con cero inicial)

    CONSTRAINT FK_Disponibilidad_Conductor
        FOREIGN KEY (idConductor) REFERENCES Conductor(idUsuario),
    CONSTRAINT FK_Disponibilidad_Vehiculo
        FOREIGN KEY (idVehiculo)  REFERENCES Vehiculo(idVehiculo)
);

-- Índices de apoyo para búsquedas por conductor y rangos
CREATE INDEX IX_Disponibilidad_Conductor ON Disponibilidad(idConductor);
CREATE INDEX IX_Disponibilidad_Vehiculo  ON Disponibilidad(idVehiculo);


