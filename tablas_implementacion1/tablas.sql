CREATE TABLE Producto (
    idProducto NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY
               CONSTRAINT PK_Producto PRIMARY KEY,
    idRestaurante NUMBER NOT NULL,
    nombre VARCHAR2(100) NOT NULL,
    precio NUMBER(10,2) NOT NULL,

